#define ADDR_V_FREERUN_AMPL_START		(ADDR_V_FREERUN_PULSE_START		+ 8)
#define ADDR_V_FREERUN_FREQ_START		(ADDR_V_FREERUN_AMPL_START		+ 8)

#define ADDR_V_TRAIN_FUNIT_START		(ADDR_V_FREERUN_FREQ_START		+ 8)
#define ADDR_V_TRAIN_PULSENAD_START		(ADDR_V_TRAIN_FUNIT_START		+ 1)
#define ADDR_V_TRAIN_AMPLNAD_START		(ADDR_V_TRAIN_PULSENAD_START	+ 1)
#define ADDR_V_TRAIN_FREQNAD_START		(ADDR_V_TRAIN_AMPLNAD_START		+ 1)
#define ADDR_V_TRAIN_DURATIONNAD_START	(ADDR_V_TRAIN_FREQNAD_START		+ 1)
#define ADDR_V_TRAIN_PULSE_START		(ADDR_V_TRAIN_DURATIONNAD_START + 1)
#define ADDR_V_TRAIN_AMPL_START			(ADDR_V_TRAIN_PULSE_START		+ 8)
#define ADDR_V_TRAIN_FREQ_START			(ADDR_V_TRAIN_AMPL_START		+ 8)
#define ADDR_V_TRAIN_DURATION_START		(ADDR_V_TRAIN_FREQ_START		+ 8)

#define ADDR_V_EXTBNC_FUNIT_START		(ADDR_V_TRAIN_DURATION_START	+ 8)
#define ADDR_V_EXTBNC_AMPLNAD_START		(ADDR_V_EXTBNC_FUNIT_START		+ 1)
#define ADDR_V_EXTBNC_AMPL_START		(ADDR_V_EXTBNC_AMPLNAD_START	+ 1)


#define ADDR_C_SINGLE_FUNIT_START		(ADDR_V_EXTBNC_AMPL_START		+ 8)
#define ADDR_C_SINGLE_PULSENAD_START	(ADDR_C_SINGLE_FUNIT_START		+ 1)
#define ADDR_C_SINGLE_AMPLNAD_START		(ADDR_C_SINGLE_PULSENAD_START	+ 1)
#define ADDR_C_SINGLE_PULSE_START		(ADDR_C_SINGLE_AMPLNAD_START	+ 1)
#define ADDR_C_SINGLE_AMPL_START		(ADDR_C_SINGLE_PULSE_START		+ 8)

#define ADDR_C_FREERUN_FUNIT_START		(ADDR_C_SINGLE_AMPL_START		+ 8)
#define ADDR_C_FREERUN_PULSENAD_START	(ADDR_C_FREERUN_FUNIT_START		+ 1)
#define ADDR_C_FREERUN_AMPLNAD_START	(ADDR_C_FREERUN_PULSENAD_START	+ 1)
#define ADDR_C_FREERUN_FREQNAD_START	(ADDR_C_FREERUN_AMPLNAD_START	+ 1)
#define ADDR_C_FREERUN_PULSE_START		(ADDR_C_FREERUN_FREQNAD_START	+ 1)
#define ADDR_C_FREERUN_AMPL_START		(ADDR_C_FREERUN_PULSE_START		+ 8)
#define ADDR_C_FREERUN_FREQ_START		(ADDR_C_FREERUN_AMPL_START		+ 8)

#define ADDR_C_TRAIN_FUNIT_START		(ADDR_C_FREERUN_FREQ_START		+ 8)
#define ADDR_C_TRAIN_PULSENAD_START		(ADDR_C_TRAIN_FUNIT_START		+ 1)
#define ADDR_C_TRAIN_AMPLNAD_START		(ADDR_C_TRAIN_PULSENAD_START	+ 1)
#define ADDR_C_TRAIN_FREQNAD_START		(ADDR_C_TRAIN_AMPLNAD_START		+ 1)
#define ADDR_C_TRAIN_DURATIONNAD_START	(ADDR_C_TRAIN_FREQNAD_START		+ 1)
#define ADDR_C_TRAIN_PULSE_START		(ADDR_C_TRAIN_DURATIONNAD_START + 1)
#define ADDR_C_TRAIN_AMPL_START			(ADDR_C_TRAIN_PULSE_START		+ 8)
#define ADDR_C_TRAIN_FREQ_START			(ADDR_C_TRAIN_AMPL_START		+ 8)
#define ADDR_C_TRAIN_DURATION_START		(ADDR_C_TRAIN_FREQ_START		+ 8)

#define ADDR_C_EXTBNC_FUNIT_START		(ADDR_C_TRAIN_DURATION_START	+ 8)
#define ADDR_C_EXTBNC_AMPLNAD_START		(ADDR_C_EXTBNC_FUNIT_START		+ 1)
#define ADDR_C_EXTBNC_AMPL_START		(ADDR_C_EXTBNC_AMPLNAD_START	+ 1)

