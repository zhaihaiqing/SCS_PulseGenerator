#define ADDR_MODE_V						0X12
#define ADDR_MODE_C						0X13

#define ADDR_V_PARAM_BASE				0X14
#define ADDR_V_SINGLE_FUNIT_START		ADDR_V_PARAM_BASE
#define ADDR_V_SINGLE_PULSENAD_START	(ADDR_V_SINGLE_FUNIT_START 		+ 1)
#define ADDR_V_SINGLE_AMPLNAD_START		(ADDR_V_SINGLE_PULSENAD_START	+ 1)
#define ADDR_V_SINGLE_PULSE_START		(ADDR_V_SINGLE_AMPLNAD_START	+ 1)		//高位在前，低位在后
#define ADDR_V_SINGLE_AMPL_START		(ADDR_V_SINGLE_PULSE_START		+ 8)

#define	ADDR_V_FREERUN_FUNIT_START		(ADDR_V_SINGLE_AMPL_START		+ 8)
